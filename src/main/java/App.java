public class App {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;
        System.out.println(sumAandB(a, b));
        System.out.println(devAandB(a, b));
    }

    public static int sumAandB (int a, int b) {
        return a + b;
    }

    public static int devAandB (int a, int b) {
        return a / b;
    }

}
